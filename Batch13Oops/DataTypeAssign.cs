using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Batch13Oops
{
    internal class DataTypeAssign
    {


        static void Main()
        {

            //Int-Datatype
            int num1 = 50;
            int num2 = 10;
            int sum = num1 + num2;
            Console.WriteLine("Age of a person is:" + sum);

            //string datatype
            string myString = "Sam";
            Console.WriteLine("Person Name is:" + myString);

            //boolean Datatype
            bool status = true;
            Console.WriteLine("Person is a Student:" + status);

            //Double datatype
            double myDouble = 12.55;
            Console.WriteLine("Double value is:" + myDouble);

            //char datatype
            char myChar = 'A';
            Console.WriteLine("Grade is:" + myChar);



        }


    }


}
